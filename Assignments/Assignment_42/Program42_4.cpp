// Structure Definition
//
////////////////////////////////////////////////////////////////////

struct node
{
    int Data;
    struct node *Next;
};

typedef struct node* PNODE;

////////////////////////////////////////////////////////////////////
//
// Function Name : Maximum
// Description   : Returns largest element from linked list
// Input         : PNODE
// Output        : Integer
// Author        : Poonam Narayan Palkar
// Date          : 14/12/2025
//
////////////////////////////////////////////////////////////////////

int Maximum(PNODE Head)
{
    int iMax = Head->Data;

    while(Head != NULL)
    {
        if(Head->Data > iMax)
        {
            iMax = Head->Data;
        }
        Head = Head->Next;
    }
    return iMax;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    PNODE First = NULL;

    First = new node{110,NULL};
    First->Next = new node{230,NULL};
    First->Next->Next = new node{320,NULL};
    First->Next->Next->Next = new node{240,NULL};

    cout << Maximum(First);

    return 0;
}

