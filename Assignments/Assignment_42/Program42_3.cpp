#include<iostream>
using namespace std;

////////////////////////////////////////////////////////////////////
//
// Structure Definition
//
////////////////////////////////////////////////////////////////////

struct node
{
    int Data;
    struct node *Next;
};

typedef struct node* PNODE;

////////////////////////////////////////////////////////////////////
//
// Function Name : Addition
// Description   : Returns addition of all elements
// Input         : PNODE
// Output        : Integer
// Author        : Poonam Narayan Palkar
// Date          : 14/12/2025
//
////////////////////////////////////////////////////////////////////

int Addition(PNODE Head)
{
    int iSum = 0;

    while(Head != NULL)
    {
        iSum = iSum + Head->Data;
        Head = Head->Next;
    }
    return iSum;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    PNODE First = NULL;

    First = new node{10,NULL};
    First->Next = new node{20,NULL};
    First->Next->Next = new node{30,NULL};
    First->Next->Next->Next = new node{40,NULL};

    cout << Addition(First);

    return 0;
}

