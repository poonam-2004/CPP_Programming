// Structure Definition
//
////////////////////////////////////////////////////////////////////

struct node
{
    int Data;
    struct node *Next;
};

typedef struct node* PNODE;

////////////////////////////////////////////////////////////////////
//
// Function Name : Minimum
// Description   : Returns smallest element from linked list
// Input         : PNODE
// Output        : Integer
// Author        : Poonam Narayan Palkar
// Date          : 14/12/2025
//
////////////////////////////////////////////////////////////////////

int Minimum(PNODE Head)
{
    int iMin = Head->Data;

    while(Head != NULL)
    {
        if(Head->Data < iMin)
        {
            iMin = Head->Data;
        }
        Head = Head->Next;
    }
    return iMin;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    PNODE First = NULL;

    First = new node{110,NULL};
    First->Next = new node{230,NULL};
    First->Next->Next = new node{20,NULL};
    First->Next->Next->Next = new node{240,NULL};
    First->Next->Next->Next->Next = new node{640,NULL};

    cout << Minimum(First);

    return 0;
}

