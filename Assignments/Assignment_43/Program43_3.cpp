struct node
{
    int Data;
    struct node *Next;
};

typedef struct node* PNODE;

////////////////////////////////////////////////////////////////////
//
// Function Name : AdditionEven
// Description   : Returns addition of all even elements
// Input         : PNODE
// Output        : Integer
// Author        : Poonam Narayan Palkar
// Date          : 14/12/2025
//
////////////////////////////////////////////////////////////////////

int AdditionEven(PNODE Head)
{
    int iSum = 0;

    while(Head != NULL)
    {
        if((Head->Data % 2) == 0)
        {
            iSum += Head->Data;
        }
        Head = Head->Next;
    }
    return iSum;
}

////////////////////////////////////////////////////////////////////
//
// Entry Point Function
//
////////////////////////////////////////////////////////////////////

int main()
{
    PNODE First = NULL;

    First = new node{11,NULL};
    First->Next = new node{20,NULL};
    First->Next->Next = new node{32,NULL};
    First->Next->Next->Next = new node{41,NULL};

    cout << AdditionEven(First);

    return 0;
}

